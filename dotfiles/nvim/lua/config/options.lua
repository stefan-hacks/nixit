-- =============================================================================
-- Options
-- =============================================================================
local opt = vim.opt

-- =============================================================================
-- General
-- =============================================================================
opt.number = true
opt.relativenumber = true
opt.cursorline = true
opt.signcolumn = "yes"
opt.colorcolumn = "80"
opt.laststatus = 3
opt.showmode = false

-- =============================================================================
-- Indentation
-- =============================================================================
opt.tabstop = 2
opt.shiftwidth = 2
opt.softtabstop = 2
opt.expandtab = true
opt.smartindent = true
opt.autoindent = true

-- =============================================================================
-- Search
-- =============================================================================
opt.ignorecase = true
opt.smartcase = true
opt.hlsearch = false
opt.incsearch = true

-- =============================================================================
-- Completion
-- =============================================================================
opt.completeopt = "menu,menuone,noselect"
opt.pumheight = 10

-- =============================================================================
-- Split behavior
-- =============================================================================
opt.splitright = true
opt.splitbelow = true

-- =============================================================================
-- Scrolling
-- =============================================================================
opt.scrolloff = 8
opt.sidescrolloff = 8

-- =============================================================================
-- Clipboard (requires wl-clipboard on Wayland or xclip on X11)
-- =============================================================================
opt.clipboard = "unnamedplus"

-- =============================================================================
-- Backup and swap
-- =============================================================================
opt.backup = false
opt.writebackup = false
opt.swapfile = false
opt.undofile = true
opt.undolevels = 10000
opt.undodir = vim.fn.stdpath("data") .. "/undo"

-- =============================================================================
-- Command height and timeout
-- =============================================================================
opt.cmdheight = 1
opt.updatetime = 250
opt.timeoutlen = 300

-- =============================================================================
-- Folding (use treesitter)
-- =============================================================================
opt.foldmethod = "expr"
opt.foldexpr = "nvim_treesitter#foldexpr()"
opt.foldlevel = 99
opt.foldlevelstart = 99

-- =============================================================================
-- Filetype detection
-- =============================================================================
vim.cmd("filetype plugin indent on")
